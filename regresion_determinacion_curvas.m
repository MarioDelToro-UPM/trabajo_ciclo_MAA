function RCA = RCA(rpm, coeff_adm)
    RCA=0;
    for stp=1:4
        RCA = RCA+coeff_adm(stp)*rpm^(4-stp);
    end
end

function AAE = AAE(rpm, coeff_esc)
    AAE=0;
    for stp = 1:4
        AAE = AAE+coeff_esc(stp)*rpm^(4-stp);
    end
end

function AICB = AICB(rpm, coeff_aicb)
    AICB=0;
    for stp=1:7
        AICB = AICB+coeff_aicb(stp)*rpm^(7-stp);
    end
end
